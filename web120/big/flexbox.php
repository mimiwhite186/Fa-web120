 <h2>Flexbox</h2>
 	<p id="pmar">Flexbox is CSS new definition layout mode. CSS has float layout can do the same thing before flexbox creation. The reason why CSS definition a new layout to instead of original layout is that the Flexbox can do better than the original.  In addition, the Flexbox has completed the specification that layouts allocate space along a single column or row. Many browsers support this layout nowadays (Vandehey, 2015).</p>
	<p id="pmar">Vandehey (2015) explains that the Flexbox layout consists of flexible containers. The main axis can be placed horizontally or vertically in the telescopic container. Because A Flexbox layout can hold flex items, developers can achieve complex layouts. As mentioned earlier, CSS Flexbox is easy to learn and is supported in most modern browsers. It makes responsive html design easier and it is great for the overall layout of a website or application. W3C specificities many flexbox properties. Carnes (2019) introduced a number of key terms for flexbox.</p>
	<p align=center><img src="images/flexbox.jpg" width="80%" alt="flexbox" /></p>
	<p align=center>Source: Carnes</p>
	<ul>
		<li id="pmar">Main-axis: The main axis is the direction in which the elastic containers are arranged. The direction is defined by the flex-direction attribute.</li>
		<li id="pmar">main-start | main-end: The starting of the flex item in the container is main-start. The ending extension of the flex item in the container is main-end.</li>
		<li id="pmar">main size: The item main size of the box are determined by the width or height of a flex container or flex item.</li>
		<li id="pmar">cross axis: The horizontal axis is the axis perpendicular to the main axis.</li>
		<li id="pmar">cross-start | cross-end: Put items fill in the container starting the cross-start side of the flex container and then toward the cross-end side.</li>
		<li id="pmar">cross size: the cross size of the item is the width or height of a flex item.</li>
	</ul>
	<p id="pmar">Because the layout of the Flexbox is very flexible, and easy to layout the blocks of web pages, it has been widely used. Therefore, beginners should spend more time learning the layout of the Flexbox.</p>
